package javapg;

public class Code14 {		// SWITCH CASE IN JAVA FOR INT

	public static void main(String[] args) {
		int x=3;
		switch(x)
		{
		case 1 :
			System.out.println("The Value of X is One");
			break;
		case 2 :
			System.out.println("The Value of X is Two");
			break;
		case 3 : 
			System.out.println(" The Value of X is Three");
			break;
		default:
			System.out.println("The Value of X is other than 1,2,3");
		}
	}


}
