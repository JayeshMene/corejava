package javapg;

public class Code13 {		// MAKING DECISION WITH IF ELSE

	public static void main(String[] args) {
		
		int x = 5;
		
	   if (x==5)
	   {
		   System.out.println("X is Equal to 5");
	   }
	   else if (x>5)
	   {
		   System.out.println("X is Greater than 5");
	   }
	   else 
	   {
		   System.out.println("X is smaller than 5");
	   }
	}

}
